#include "header.h"

int		ft_pourcent(const char *s, int *i)
{
	*i += 1;
	if (ft_strchr(ft_strdup("*#0-+hljz0 123456789."), s[*i]) != 0)
	{
		while (ft_strchr(ft_strdup("*#0-+hljz0 123456789."), s[*i]) != 0)
			*i += 1;
		if (s[*i] == '%')
			return (1);
	}
	return (0);
}
