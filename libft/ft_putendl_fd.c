#include "libft.h"

void	ft_putendl_fd(char const *c, int fd)
{
	int i;

	i = 0;
	while (c[i])
	{
		ft_putchar_fd(c[i], fd);
		i++;
	}
	ft_putchar_fd('\n', fd);
}
